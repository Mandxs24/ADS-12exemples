public class exemplo7 {
	public static void main(String[] args) {
	  String nome = "Pedro"; // Definindo nome
	  int a = 6; // Definindo a
	  a += 2; // Somou + 2. Agora, a = 8
	  a += 3; // Somou + 3. Agora, a = 11
	  a += 4; // Somou + 4. Agora, a = 15
	  a += 2; // Somou + 2. Agora, a = 17
	  a -= 6; // Diminuiu 6. Agora, a = 11
	  double b = 12.0; // Definindo b
	  b += 5.0; // Somou + 5.0 Agora, b = 17
	  double c = 2.0; // Definindo c
	  c *= 3; // Multiplicou por 3. Agora, c = 6.0
	  System.out.println("Meu segundo nome é: " + nome);
	  System.out.println("O primeiro valor é: " + a);
	  System.out.println("O segundo valor é: " + b);
	  System.out.println("O terceiro valor é: " + c);

 }
}
