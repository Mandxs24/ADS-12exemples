package exemplos12;

public class Exemplo10 {
	public static void main(String[] args) {
		String nome = "Carlos"; // Definindo nome
		int a = 4; // Definindo a
		a += 3; // Somou + 3. Agora, a = 7
		a += 2; // Somou + 2. Agora, a = 9
		a += 1; // Somou + 1. Agora, a = 10
		a += 4; // Somou + 4. Agora, a = 14
		a -= 3; // Diminuiu 3. Agora, a = 11
		double b = 8.5; // Definindo b
		b += 3.5; // Somou + 3.5 Agora, b = 12
		double c = 6.7; // Definindo c
		c *= 2; // Multiplicou por 2. Agora, c = 13.4
		System.out.println("Meu segundo nome é: " + nome);
		System.out.println("O primeiro valor é: " + a);
		System.out.println("O segundo valor é: " + b);
		System.out.println("O terceiro valor é: " + c);

	}
}
