package exemplos12;

public class exemplo9 {
	public static void main(String[] args) {
		String nome = "Arthur"; // Definindo nome
		int a = 7; // Definindo a
		a += 3; // Somou + 3. Agora, a = 10
		a += 5; // Somou + 5. Agora, a = 15
		a -= 2; // Diminuiu 2. Agora, a = 13
		a += 4; // Somou + 4. Agora, a = 17
		a -= 6; // Diminuiu 6. Agora, a = 11
		double b = 25.0; // Definindo b
		b -= 10.0; // Diminuiu 10.0. Agora, b = 15
		double c = 8.0; // Definindo c
		c /= 4; // Dividiu por 4. Agora, c = 2.0
		System.out.println("Meu segundo nome é: " + nome);
		System.out.println("O primeiro valor é: " + a);
		System.out.println("O segundo valor é: " + b);
		System.out.println("O terceiro valor é: " + c);
	}
}
