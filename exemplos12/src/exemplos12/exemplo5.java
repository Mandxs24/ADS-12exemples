package exemplos12;

public class exemplo5 {
	public static void main(String[] args) {
		String nome = "Beatriz"; // Definindo nome
		int x = 5; // Definindo x
		x += 10; // Somou + 10. Agora, x = 15
		x += 5; // Somou + 5. Agora, x = 20
		x -= 8; // Diminuiu 8. Agora, x = 12
		int y = 50; // Definindo y
		y -= 15; // Diminuiu 15. Agora, y = 35
		y += 7; // Somou + 7. Agora, y = 42
		double w = 3.7; // Definindo w
		w += 2.3; // Somou + 2.3. Agora, w = 6.0
		w *= 2; // Multiplicou por 2. Agora, w = 12.0
		System.out.println("Meu nome é: " + nome);
		System.out.println("O primeiro valor é: " + x);
		System.out.println("O segundo valor é: " + y);
		System.out.println("O terceiro valor é: " + w);

	}
}
