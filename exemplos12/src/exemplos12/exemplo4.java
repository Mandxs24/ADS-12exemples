package exemplos12;

public class exemplo4 {
	public static void main(String[] args) {
		String nome = "Amanda"; // Definindo nome
		int a = 2; // Definindo a
		a += 3; // Somou + 3. Agora, a = 5
		a += 7; // Somou + 7. Agora, a = 12
		a -= 4; // Diminuiu 4. Agora, a = 8
		int b = 30; // Definindo b
		b -= 10; // Diminuiu 10. Agora, b = 20
		b += 5; // Somou + 5. Agora, b = 25
		double c = 9.5; // Definindo c
		c += 4.5; // Somou + 4.5. Agora, c = 14.0
		c -= 2.0; // Diminuiu 2.0. Agora, c = 12.0
		System.out.println("Meu nome é: " + nome);
		System.out.println("O primeiro valor é: " + a);
		System.out.println("O segundo valor é: " + b);
		System.out.println("O terceiro valor é: " + c);

	}
}
