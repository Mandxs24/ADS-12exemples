package exemplos12;

public class exemplo6 {
	public static void main(String[] args) {
		String nome = "Amandinha"; // Definindo nome
		int p = 8; // Definindo p
		p += 6; // Somou + 6. Agora, p = 14
		p += 9; // Somou + 9. Agora, p = 23
		p -= 3; // Diminuiu 3. Agora, p = 20
		int q = 100; // Definindo q
		q -= 40; // Diminuiu 40. Agora, q = 60
		q /= 2; // Dividiu por 2. Agora, q = 30
		double r = 12.2; // Definindo r
		r += 5.8; // Somou + 5.8. Agora, r = 18.0
		r /= 3; // Dividiu por 3. Agora, r = 6.0
		System.out.println("Meu apelido é: " + nome);
		System.out.println("O primeiro valor é: " + p);
		System.out.println("O segundo valor é: " + q);
		System.out.println("O terceiro valor é: " + r);

	}
}
