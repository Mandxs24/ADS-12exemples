package exemplos12;

public class exemplo8 {
 public static void main (String[] args) {
	  String nome = "Bruna"; // Definindo nome
	  int a = 4; // Definindo a
	  a -= 5; // Diminuiu 5. Agora, a = -1
	  a += 7; // Somou + 7. Agora, a = 6
	  a += 3; // Somou + 3. Agora, a = 9
	  a -= 1; // Diminuiu 1. Agora, a = 8
	  a += 2; // Somou + 2. Agora, a = 10
	  double b = 15.0; // Definindo b
	  b -= 6.0; // Diminuiu 6.0. Agora, b = 9
	  double c = 5.0; // Definindo c
	  c *= 2; // Multiplicou por 2. Agora, c = 10.0
	  System.out.println("Meu segundo nome é: " + nome);
	  System.out.println("O primeiro valor é: " + a);
	  System.out.println("O segundo valor é: " + b);
	  System.out.println("O terceiro valor é: " + c);
 }
}
