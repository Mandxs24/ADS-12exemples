package exemplos12;

public class Exemplo11 {
	public static void main(String[] args) {
		String nome = "Joana"; // Definindo nome
		int a = 2; // Definindo a
		a += 4; // Somou + 4. Agora, a = 6
		a += 2; // Somou + 2. Agora, a = 8
		a += 3; // Somou + 3. Agora, a = 11
		a += 1; // Somou + 1. Agora, a = 12
		a -= 4; // Diminuiu 4. Agora, a = 8
		double b = 7.0; // Definindo b
		b += 4.0; // Somou + 4.0 Agora, b = 11
		double c = 3.3; // Definindo c
		c /= 2; // Dividiu por 2. Agora, c = 1.65
		System.out.println("Meu segundo nome é: " + nome);
		System.out.println("O primeiro valor é: " + a);
		System.out.println("O segundo valor é: " + b);
		System.out.println("O terceiro valor é: " + c);
	}
}
