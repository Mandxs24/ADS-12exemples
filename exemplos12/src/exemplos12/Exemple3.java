package exemplos12;

public class Exemplo3 {
	public static void main(String[] args) {
		String nome = "AnaLu"; // Definindo nome
		int z = 1; // Definindo z
		z += 2; // Somou + 2. Agora, z = 3 
		z += 10; // Somou + 10. Agora, z = 13
		z += 3; // Somou + 3. Agora, z= 16
		z += 4; // Somou + 4. Agora, z = 20
		z -= 5; // Diminuiu 5.Agora, z = 15
		int T = 25; // Definindo T
		T -= 5; // Diminuiu 5. Agora, T = 20
		double u = 7.5; // Definindo u
		u += 3; // Somou + 3. Agora, u = 10.5
		System.out.println("Meu apelido é: " + nome);
		System.out.println("O primeiro valor é: " + z);
		System.out.println("O segundo valor é: " + T);
		System.out.println("O quarto valor é: " + u);

	}
}
